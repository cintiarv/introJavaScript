//Generar una funcion que nos ayude a retornar la suma de todos los numeros que hay dentro de los parametros sumAll(1, 4) // 1 + 2 + 3 + 4 = 10

function sumAll (num1,num2){
    let suma = 0;
    for(let i=num1; i=num2; i++){
        suma = num1+num2;
        return suma;
    }
}

let result = sumAll(12,5);
console.log(result);
